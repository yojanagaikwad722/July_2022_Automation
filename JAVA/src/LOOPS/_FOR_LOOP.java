package LOOPS;

public class _FOR_LOOP {

	public static void main(String[] args) {
		//Print 1 to 10 numbers using for loop
		
		//start no. - 1
		//end no. - 10
		//increment
		//condition - relation between start no & end no - <=
		
		for(int i=1; i<=10; i++) {
			System.out.println(i);//1 2 3.......9 10
		}
		
	}
}
