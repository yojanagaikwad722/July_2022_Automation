package STRING;

public class example {

	//final -> we cant inherit to other classes
	public static void main(String[] args) {
		String s1 = "quantum academy";//without using new keyword
		
		String s2 = new String("quantum academy");//object creation
		
		String s3 = "quantum academy";
		
		String s4 = "quantum";
		
		String s5 = new String("quantum academy");
		
		String s6 = new String("quantum");
	}
}
